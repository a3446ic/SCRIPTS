																	REPLACE(
																		REPLACE(
																			REPLACE(
																				REPLACE(
																					REPLACE(
																						REPLACE(
																							REPLACE(
																								REPLACE(
																									REPLACE(
																										REPLACE(
																											REPLACE(
																												iban_numeric,
																												'A', '10'
																											),
																											'B', '11'
																										),
																										'C', '12'
																									),
																									'D', '13'
																								),
																								'E', '14'
																							),
																							'F', '15'
																						),
																						'G', '16'
																					),
																					'H', '17'
																				),
																				'I', '18'
																			),
																			'J','19'
																		),
																		'K', '20'
																	),
																	'L', '21'
																),
																'M', '22'
															),
															'N', '23'
														),
														'O', '24'
													),
													'P', '25'
												),
